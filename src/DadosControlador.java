
public class DadosControlador {

	private double kp;
	private double ki;
	private double kd;

	private double talI;
	private double talD;	
	private double talT;
	
	public double getKp() {
		return kp;
	}
	public void setKp(double kp) {
		this.kp = kp;
	}
	public double getKi() {
		return ki;
	}
	public void setKi(double ki) {
		this.ki = ki;
	}
	public double getKd() {
		return kd;
	}
	public void setKd(double kd) {
		this.kd = kd;
	}
	public double getTalI() {
		return talI;
	}
	public void setTalI(double talI) {
		this.talI = talI;
	}
	public double getTalD() {
		return talD;
	}
	public void setTalD(double talD) {
		this.talD = talD;
	}
	public double getTalT() {
		return talT;
	}
	public void setTalT(double talT) {
		this.talT = talT;
	}
}
